- (void)dealloc
{
    [_window release];
    [_viewController release];
	[_storeController release];
    [super dealloc];
}

- (BOOL)application:(UIApplication *)application didFinishLaunchingWithOptions:(NSDictionary *)launchOptions
{
    self.window = [[[UIWindow alloc] initWithFrame:[[UIScreen mainScreen] bounds]] autorelease];
    // Override point for customization after application launch.
    self.viewController = [[[MainViewController alloc] initWithNibName:@"MainViewController" bundle:nil] autorelease];
	self.storeController = [[[PCStoreController alloc] initWithStoreRootViewController:self.viewController] autorelease];
	//self.viewController.storeController = _storeController;
	//[_storeController launch];

	UINavigationController* navControler = [[UINavigationController alloc] initWithRootViewController:self.viewController];
	navControler.navigationBar.hidden = YES;
	self.window.rootViewController = [navControler autorelease];//self.viewController.storeController.navigationController;
	[self.window makeKeyAndVisible];
	
    return YES;
}

